    public void loadListBook()
    {
        this.model.setRowCount(0);
        try{
            String sqlGetListBook = "SELECT * FROM `khosach` ORDER BY book_id";
            String sqlGetTacGia = "SELECT * FROM `user` ORDER BY `user_id`";
            Statement stmt = services.cnn.con.createStatement();
            ResultSet rs = stmt.executeQuery(sqlGetListBook);
            while(rs.next())
            {   
                Model_Sach tmp = new Model_Sach(rs);
                this.model.addRow(new Object[]{tmp.getID(), tmp.getTen(), services.cnn.getRowInDB("danhmuc", "id", tmp.getTheLoai().trim()).getString("ten"), tmp.getSoLuong()});        
            }
            rs = stmt.executeQuery(sqlGetTacGia);
            while(rs.next())
            {
                txtTacGia.addItem(rs.getString("Ten"));
            }
        }
        catch(Exception e)
        {
            services.msgbox("Error", "Có lỗi trong quá trình lấy sách!");
            services.msg("PanelManageBook loadListBook(): "+e);
        }
    }

    public void loadBook(String id)
    {
        Model_Sach sach = services.getBookByID(id);
        if(sach==null)
        {
            return;
        }
        try{
            txtID.setText(sach.getID());
            txtTacGia.setSelectedItem(services.cnn.getRowInDB("user", "user_id", sach.getIdTacGia()).getString("Ten"));
            txtTen.setText(sach.getTen());
            txtTheLoai.setSelectedItem(services.cnn.getRowInDB("danhmuc", "id", sach.getTheLoai().trim()).getString("ten"));
            txtGia.setText(sach.getGia()+"");
            txtSL.setText(sach.getSoLuong()+"");
            txtImage.setText(sach.getImage());

            BufferedImage img = ImageIO.read(new URL(services.getUrlImageBook(sach.getImage())));
            ImageIcon icon = new ImageIcon(img.getScaledInstance(150, 200, WIDTH));
            imageBook.setIcon(icon);
        }
        catch(Exception e)
        {
            services.msgbox("Error","Không thể lấy thông tin sách");
            services.msg(e);
        }
                
    }
