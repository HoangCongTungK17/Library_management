    private void btnRemoveAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveAccountActionPerformed
        // TODO add your handling code here:
        //Remove Account
        if(Integer.parseInt(this.idRemove)==-1)
        {
            return;
        }
        try{
            Statement stmt = services.cnn.con.createStatement();
            stmt.execute("DELETE FROM `user` WHERE user_id='"+this.idRemove+"'");
            stmt.execute("DELETE FROM `login` WHERE user_id='"+this.idRemove+"'");
            services.msgbox("Success", "Xóa user thành công!");
            services.sendLog(services.user.getID(),services.user.getTen(),"removeuser",Message.removeUser+this.idRemove);
            loadUser();
        }
        catch(Exception e)
        {
            services.msgbox("Error","Có lỗi trong quá trình xóa tài khoản này");
            services.msg(e);
        }
    }//GEN-LAST:event_btnRemoveAccountActionPerformed

