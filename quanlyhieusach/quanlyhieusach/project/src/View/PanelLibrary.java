    private void loadShelfBook()
    {
        ArrayList<Model_Sach> mostView = new ArrayList<Model_Sach>();
        ArrayList<Model_Sach> mostBuy = new ArrayList<Model_Sach>();
        try{
            String sqlMostView = "SELECT * FROM `khosach` WHERE 1 ORDER BY `luotxem` DESC LIMIT 50";
            Statement stmt = services.cnn.con.createStatement();
            ResultSet rs = stmt.executeQuery(sqlMostView);
            while(rs.next())
            {
                mostView.add(new Model_Sach(rs));
            }
            String sqlMostBuy = "SELECT * FROM `khosach` WHERE 1 ORDER BY `luotmua` DESC LIMIT 50";
            rs = stmt.executeQuery(sqlMostBuy);
            while(rs.next())
            {
                mostBuy.add(new Model_Sach(rs));
            }
            labelBook1.setText("Sách xem nhiều");
            services.changePanel(panelBook1, new PanelShelfBook(mostView), 1237, 225);
            
            labelBook2.setText("Sách mua nhiều");
            services.changePanel(panelBook2, new PanelShelfBook(mostBuy), 1237, 225);
        }
        catch(Exception e)
        {
            services.msg(e);
        }
    }
