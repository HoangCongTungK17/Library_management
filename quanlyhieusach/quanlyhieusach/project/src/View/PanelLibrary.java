    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        if(((String)comboboxFilter.getSelectedItem()).compareTo("Phân loại")==0)
        {
            services.msgbox("Thông báo",Message.chooseFilter);
            return;
        }
        ArrayList<Model_Sach> results = new ArrayList<Model_Sach>();
        if(((String)comboboxFilter.getSelectedItem()).compareTo("Theo ID")==0)
        {
            try{
                String sql = "SELECT * FROM `khosach` WHERE `book_id`='"+txtKeyWord.getText().trim()+"'";
                Statement stmt = services.cnn.con.createStatement();
                ResultSet rs = stmt.executeQuery(sql);
                while(rs.next())
                {
                    Model_Sach tmp =  new Model_Sach(rs);
                    results.add(tmp);
                }
            }
            catch(Exception e)
            {
                services.msg(e);
            }
        }
        else{
            try{
            String sql = "SELECT * FROM `khosach` WHERE 1";
            Statement stmt = services.cnn.con.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while(rs.next())
            {
                Model_Sach tmp =  new Model_Sach(rs);
                if(tmp.getTen().toLowerCase().contains(txtKeyWord.getText().trim().toLowerCase()))
                results.add(tmp);
            }
            }
            catch(Exception e)
            {
                services.msg(e);
            }
        }
        if(results.size()>0)
                    services.changePanel(this, new PanelResult(results), 1420, 748);
        else
            services.msgbox("Error", Message.notFoundResultBook);
    }//GEN-LAST:event_btnFindActionPerformed

    private void comboboxFilterItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboboxFilterItemStateChanged
        // TODO add your handling code here:
        String value = (String)comboboxFilter.getSelectedItem();
        if(value.compareTo("Theo ID")==0||value.compareTo("Phân loại")==0||value.compareTo("Theo tên")==0)
        {
            return;
        }
        try{
            ArrayList<Model_Sach> list = new ArrayList<Model_Sach>();
            String sql = "SELECT * FROM `khosach` WHERE `theloai` = '"+services.cnn.getRowInDB("danhmuc", "ten", value).getString("id")+"'";
            ResultSet rs = services.cnn.con.createStatement().executeQuery(sql);
            while(rs.next())
            {
                list.add(new Model_Sach(rs));
            }
            services.changePanel(this, new PanelResult(list), 1420, 748);
        }
        catch(Exception e)
        {
            services.msg(e);
        }
    }//GEN-LAST:event_comboboxFilterItemStateChanged


