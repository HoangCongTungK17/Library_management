    private void btnAddBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddBookActionPerformed

        if(txtboxTen.getText().compareTo("")==0||txtboxGia.getText().compareTo("")==0||txtboxLinkAnh.getText().compareTo("")==0||txtboxSoLuong.getText().compareTo("")==0)
        {
            services.msgbox("Thiếu thông tin", "Vui lòng điền đầy đủ thông tin!");
            return;
        }
        if(services.user instanceof People_NhanVien)
        {
            ((People_NhanVien) services.user).addBook(this.last_id,txtboxTen.getText(),(String)txtboxTacGia.getSelectedItem(),(String)txtboxTheLoai.getSelectedItem(), txtboxGia.getText(), txtboxSoLuong.getText(), txtboxLinkAnh.getText());
        }
        else {
            ((People_QuanLy) services.user).addBook(this.last_id,txtboxTen.getText(),(String)txtboxTacGia.getSelectedItem(),(String)txtboxTheLoai.getSelectedItem(), txtboxGia.getText(), txtboxSoLuong.getText(), txtboxLinkAnh.getText());
        }
    }//GEN-LAST:event_btnAddBookActionPerformed


