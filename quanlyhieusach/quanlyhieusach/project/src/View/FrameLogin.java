    public void login() //throws InterrupptedException 
    {
        services.cnn.connect();
        if(services.user.login(tk.getText(),String.valueOf(mk.getPassword())))
        {
            switch(services.user.getChucvu())
            {
                case "Manager" :{
                    services.msg("Manager login");
                    services.user = new People_QuanLy(services.user);
                    break;
                }
                case "Customer" :{
                    services.msg("Customer login");
                    services.user = new People_Khach(services.user);
                    try{
                        
                        ResultSet rs = services.cnn.con.createStatement().executeQuery("SELECT * FROM `inventory` WHERE user_id='"+services.user.getID()+"'");
                        if(rs.next())
                        {
                            ((People_Khach)services.user).loadInventory(rs,"inventory");
                            ((People_Khach)services.user).loadInventory(rs,"cart");
                        }
                        
                    }
                    catch(Exception e) {
                        System.out.println("Khong the lay array");
                    }
                    
                    break;
                }
                case "Staff" :{
                    services.msg("Staff login");
                    services.user = new People_NhanVien(services.user);
                    break;
                }
                case "Author" :{
                    services.msg("Author login");
                    break;
                }
                default :{
                    services.msg("Other login");
                    break;
                }
            }
            
            services.cnn.loadBook();
            FrameMainMenuStaff home = new FrameMainMenuStaff(null);
            home.show();
            dispose();
        }
    }
