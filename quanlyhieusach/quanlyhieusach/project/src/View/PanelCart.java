    private void btnBuyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuyActionPerformed
        // TODO add your handling code here:
        if(!(services.user instanceof People_Khach))
        {
            return;
        }
        if(txtID.getText().compareTo("")==0) return;
        try{
            for(Model_Sach sach:((People_Khach)services.user).getInventory("cart"))
            {
                if(sach.getID().compareTo(txtID.getText())==0)
                {
                    if(services.user.getSoDu()>=sach.getGia()*sach.getSoLuong())
                    {
                        ((People_Khach)services.user).buyBook(sach.getGia()*sach.getSoLuong());
                        boolean isExist = false;
                        for(Model_Sach item :((People_Khach)services.user).getInventory("inventory"))
                        {
                            if(sach.getID().compareTo(item.getID())==0)
                            {
                                item.setSoLuong(item.getSoLuong()+sach.getSoLuong());
                                isExist = true;
                                break;
                            }
                        }
                        if(!isExist) ((People_Khach)services.user).getInventory("inventory").add(sach);
                        ((People_Khach)services.user).getInventory("cart").remove(sach);
                        ((People_Khach)services.user).updateInventory("cart");
                        ((People_Khach)services.user).updateInventory("inventory");
                        this.loadCartUser();
                        this.clearItem();
                        services.msgbox("Success","Mua sach thanh cong");
                        services.buyBook(sach.getID(), sach.getSoLuong());
                        txtSL.setText((Integer.parseInt(txtSL.getText())-sach.getSoLuong())+"");
                        services.sendLog(services.user.getID(),services.user.getTen(),"buy", Message.buyBook +sach.getID());
                        break;
                    }
                    else return ;
                }
            }
        }
        catch(Exception e)
        {
            services.msg(e);
        }
    }//GEN-LAST:event_btnBuyActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        
        if(txtID.getText().compareTo("")==0) return;
        {
            try{
                for(Model_Sach item:((People_Khach)services.user).getInventory("cart"))
                {
                    if(item.getID().compareTo(txtID.getText())==0)
                    {
                        ((People_Khach)services.user).getInventory("cart").remove(item);
                        break;
                    }
            }
            ((People_Khach)services.user).updateInventory("cart");
            this.loadCartUser();
            this.clearItem();
            }
            catch(Exception e)
            {
                services.msg("Panel Cart");
                services.msg(e);
            }
        }   
    }//GEN-LAST:event_btnRemoveActionPerformed

