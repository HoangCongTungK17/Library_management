    public void loadCartUser()
    {
        model.setNumRows(0);
        try{
            String sql = "SELECT * FROM `inventory` WHERE user_id='"+services.user.getID()+"'";
            ResultSet rs = services.cnn.con.createStatement().executeQuery(sql);
            if(rs.next())
            {        
                if(services.user instanceof People_Khach)
                {
                    //services.msg("Load cart in menu");
                    ((People_Khach)services.user).loadInventory(rs, "cart");
                }
            }
            for(Model_Sach sach : ((People_Khach)services.user).getInventory("cart"))
            {
                //services.msg("Add row : "+sach.getTen());
                model.addRow(new Object[]{sach.getID(), sach.getTen(), services.cnn.getRowInDB("danhmuc", "id", sach.getTheLoai().trim()).getString("ten"), sach.getSoLuong()});
            }
        }
        catch(Exception e)
        {
            services.msg(e);
        }
    }
    public void loadBook(String id)
    {
     Model_Sach sach = services.getBookByID(id);   
     if(sach==null) return;
        {
            try{
                txtID.setText(sach.getID());
                txtTacGia.setText(services.cnn.getRowInDB("user", "user_id", sach.getIdTacGia()).getString("Ten"));
                txtTen.setText(sach.getTen());
                txtTheLoai.setText(services.cnn.getRowInDB("danhmuc", "id", sach.getTheLoai().trim()).getString("ten"));
                txtGia.setText(sach.getGia()+"");
                txtSL.setText(sach.getSoLuong()+"");

                BufferedImage img = ImageIO.read(new URL(services.getUrlImageBook(sach.getImage())));
                ImageIcon icon = new ImageIcon(img.getScaledInstance(150, 200, WIDTH));
                imageBook.setIcon(icon);
                }
            catch(Exception e)
            {
                services.msgbox("Error","Không thể lấy thông tin sách");
                services.msg(e);
            }
        }
    }
