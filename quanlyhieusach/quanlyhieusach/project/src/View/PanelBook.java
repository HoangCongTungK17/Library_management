    private void btnBuyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuyActionPerformed
        // TODO add your handling code here:
        if(!(services.user instanceof People_Khach))
        {
            return;
        }
        if(txtID.getText().compareTo("")==0) return;
        Model_Sach book = services.getBookByID(txtID.getText().split(":")[1].trim());
        if(book==null){
            services.msg("Book null");
            return;
        }
        book.setSoLuong(1);
        if(services.user.getSoDu()>=book.getGia())
        {
            ((People_Khach)services.user).buyBook(book.getGia());
            boolean isExist = false;
            for(Model_Sach item :((People_Khach)services.user).getInventory("inventory"))
            {
                if(book.getID().compareTo(item.getID())==0)
                {
                    item.setSoLuong(item.getSoLuong()+1);
                    isExist = true;
                    break;
                }
            }
            if(!isExist) ((People_Khach)services.user).getInventory("inventory").add(book);
            ((People_Khach)services.user).updateInventory("cart");
            ((People_Khach)services.user).updateInventory("inventory");
            services.buyBook(book.getID(), 1);
            txtSoLuong.setText("Số lượng : "+(Integer.parseInt(txtSoLuong.getText().split(":")[1].trim())-1));
            if(Integer.parseInt(txtSoLuong.getText().split(":")[1].trim())==0)
            {
                btnBuy.setEnabled(false);
                btnAddToCart.setEnabled(false);
            }
            services.msgbox("Success","Mua sach thanh cong");
            this.loadComment();
            services.sendLog(services.user.getID(),services.user.getTen(),"buy", Message.buyBook +book.getID());
        }
    }//GEN-LAST:event_btnBuyActionPerformed

