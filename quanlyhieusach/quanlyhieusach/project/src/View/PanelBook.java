    private void loadComment()
    {
        boolean isUser = false;
        if(services.user instanceof People_Khach)
        {
            isUser = true;
            for(Model_Sach sach:((People_Khach)services.user).getInventory("inventory"))
            {
                if(sach.getID().compareTo(txtID.getText().split(":")[1].trim())==0)
                {
                    btnSubmitRate.setEnabled(true);
                    break;
                }
            }
        }
        float rate = 0;
        int count = 0;
        this.model.setNumRows(0);
        try{
            String sql = "SELECT * FROM `rate` WHERE `book_id`='"+txtID.getText().split(":")[1].trim()+"'";
            ResultSet rs = services.cnn.con.createStatement().executeQuery(sql);
            while(rs.next())
            {
                if(isUser && !rated && rs.getString("user").compareTo(services.user.getID())==0) this.rated = true;
                this.model.addRow(new Object[]{rs.getDate("time"), services.cnn.getRowInDB("user", "user_id", rs.getString("user")).getString("Ten"),rs.getString("comment"),rs.getInt("star")});
                rate += rs.getInt("star");
                count +=1;
            }
            if(count!=0)
            labelGeneralStar.setText(String.format("%.1f",(float)rate/count));
            else labelGeneralStar.setText("0");
            if(this.rated)
            {
                txtRate.setText("Bạn đã để lại đánh giá sách này.");
                txtRate.setEditable(false);
                btnSubmitRate.setEnabled(false);
            }
        }
        catch(Exception e)
        {
            services.msg(e);
        }
    }
